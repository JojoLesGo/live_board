import 'package:flutter/material.dart';
import 'customWidgets.dart';

enum AuthStatus {
  signedIn,
  emailAddress,
  password,
  registration,
}

class Login extends StatelessWidget {
  const Login(
      {required this.signOnState,
      required this.email,
      required this.checkEmail,
      required this.signOn,
      required this.cancelSignUp,
      required this.signUp,
      required this.signOut});

  final AuthStatus signOnState;
  final String? email;
  final void Function(
    String email,
    void Function(Exception e) error,
  ) checkEmail;
  final void Function(
    String email,
    String pass,
    void Function(Exception e) err,
  ) signOn;
  final void Function() cancelSignUp;
  final void Function(
    String email,
    String displayName,
    String password,
    void Function(Exception e) error,
  ) signUp;
  final void Function() signOut;

  @override
  Widget build(BuildContext context) {
    switch (signOnState) {
      case AuthStatus.emailAddress:
        return EmailForm(
            callback: (email) => checkEmail(
                email, (e) => displayErr(context, 'Invalid email', e)));
      case AuthStatus.password:
        return PasswordForm(
          email: email!,
          login: (email, password) {
            signOn(email, password,
                (e) => displayErr(context, 'Failed to sign in', e));
          },
        );
      case AuthStatus.registration:
        return RegisterForm(
          email: email!,
          cancel: () {
            cancelSignUp();
          },
          registerAccount: (
            email,
            displayName,
            password,
          ) {
            signUp(email, displayName, password,
                (e) => displayErr(context, 'Failed to create account', e));
          },
        );
      case AuthStatus.signedIn:
        WidgetsBinding.instance.addPostFrameCallback((_) {
          Navigator.of(context).push(MaterialPageRoute(builder: (context) => const CapitalsChatBot(key: Key("123"), title: 'Chattie')));
        });
        return Row();
      default:
        return Row(
          children: const [
            Text('Internal error, oops'),
          ],
        );
    }
  }

  void displayErr(BuildContext context, String title, Exception e) {
    showDialog<void>(
      context: context,
      builder: (context) {
        return AlertDialog(
          title: Text(
            title,
            style: const TextStyle(fontSize: 24),
          ),
          content: SingleChildScrollView(
            child: ListBody(
              children: <Widget>[
                Text(
                  '${(e as dynamic).message}',
                  style: const TextStyle(fontSize: 18),
                ),
              ],
            ),
          ),
          actions: <Widget>[
            NiceButton(
              onClick: () {
                Navigator.of(context).pop();
              },
              child: const Text(
                'OK',
                style: TextStyle(color: Colors.deepPurple),
              ),
            ),
          ],
        );
      },
    );
  }
}

class EmailForm extends StatefulWidget {
  const EmailForm({required this.callback});
